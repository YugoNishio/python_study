me = int(input("”先手”グー[0],チョキ[1],パー[2]のうちどれかを入力してください-->"))
me_2 = int(input("”後手”グー[0],チョキ[1],パー[2]のうちどれかを入力してください-->"))

if me == me_2:
    print("あいこです")
elif ((me == 0) and (me_2 == 1)) or ((me == 1) and (me_2 == 2)):
    print("先手勝利")
elif ((me == 2) and (me_2 == 1)) or ((me == 1) and (me_2 == 0)):
    print("後手勝利")